    <td>  <h3>BIO</h3></td>
  </tr>
  <tr>
    <td>rana salam is one of the most established and celebrated graphic designers and artists from

the Middle East. Her unique knowledge of Arab culture and its popular art stems from years
of travelling and documenting. She then merges her collected ephemera and imagery with the
latest technology to create a unique vision of graphic design and art direction, her signature
language.
Rana has been running her own London and now Beirut based design studio for over a
decade, producing distinctive designs for clients which include Harvey Nichols, Villa Moda,

Liberty of London, Bokja design, Boutique 1, the Victoria and Albert Museum (V&A), Arts
Council England, and Paul Smith.
