                    <td><p>The fountain &quot;chrob ou chouf&quot; near Ben Youssef Mosque in the heart of the medina of Marrakech, was brought in Ahmed El Mansour (1578-1603). One of the inscriptions carved invite to &quot;drink and watch&quot; (in Arabic &quot;echroub ou chouf&quot;). More than just a fountain, this space invites people to take the time to enjoy life and watch what is happening around them.<br />
                      The idea of the workshop will be to create a seating area (urban furniture, chairs, sofas... ) that suggest a pause in the daily activities. <br />
                      This seat needs to be easy to use and convenient. It should be conforatble and ergonomic.</p>
