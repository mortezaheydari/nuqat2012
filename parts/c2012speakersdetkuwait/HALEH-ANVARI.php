                    <td><p>We are a region of tall walls and multifarious hejabs. Our veiled view of the world and the worlds view of us result in a disconnect that only citizen documentation can clarify.
Through my conceptual photography, I initially tried to change the perception of the local hejab (chador) which I felt was unfairly represented artistically as it was a symbol of my country for many years. Realizing that the view of a few artists was not enough, I created www.aksbazi.com, a digital space where ordinary Iranians could enter the photographic realm in order to document their own interpretation of their daily lives, one that is often hidden from view, filling in the gaps of the pixelated image of their nation.
