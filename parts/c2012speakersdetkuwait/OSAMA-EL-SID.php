                    <td><h3>WORKSHOP</h3></td>
                  </tr>
                  <tr>
                    <td><span style="color:#13969E;line-height:12pt;font-size:15pt;">Print Anything Anywhere</span></td>
                  </tr>
                                    <tr>
                    <td><p>If you are a photographer, designer, mixed media artist or anyone just interested in experimenting, this workshop is the right one for you. With Osama you will explore an alternative method to develop and print in a completely economical way and more surprisingly, it can be done anywhere.
<br/><br/>
In this workshop we shall use everyday items (such as vacuum cleaners for example) and make use of the sunlight to develop. By applying Vandyke emulsion, a method which will be taught during the session, you shall use any material of choice, be it glass, paper, wood or cloth to develop your image.
<br/><br/>
We will explore mixing calligraphy, patterns, imagery and prints all applied on different raw material creating a limitless bar to our creativity thus providing you with the tool to develop your work on anything, anywhere and anytime.
<br/><br/>
The whole point of this workshop is to introduce a technique that is very economical and cheap to use in order to cultivate the artists' skills and give them the creativity to experiment and develop anything on ANYTHING.</p>
