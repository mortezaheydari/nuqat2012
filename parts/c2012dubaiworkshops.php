    <td style="padding-top:5px;color:#636362;">How can we map Dubai in nonvisual
terms? This workshop will aim
to challenge the traditional methods
of cartography and find new
ways to see and represent the city.
Mapping, typically, a visual representation
tool will be contested. We
will find ways to symbolically depict
and highlight the relationships between
elements of space through
non-visual maps. We will look to
translate spatial experiences that
are non- visual to non-visual representation.
How do you represent
smell with abstracted smell? And
how do you represent memory in
this transient city?
This workshop is open to writers, designers
and artists or anyone who
has an interest in the city.</td>
