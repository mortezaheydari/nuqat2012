    <td style="padding-top:5px;color:#636362;">The Workshop will grant the participants the comprehension of Arabic Kufic patterns

and will enable them to create their own Square and/or Floral Kufic patterns. The

workshop will begin with a talk introducing the three main Kufic calligraphic styles:

Archaic Kufic, Floral Modern Kufic and Square Kufic, and then it will move on to

become a Hands-On drawing session of arabesque patterns. Participants will create

arabesque typographic patterns from both the Floral and Square Kufic Letterforms

and structures. Besides acquiring the technique of drawing Kufic, by the end of the

workshop participants will be able to decipher Geometric Kufic patterns into words

and sentences instead of perceiving them as abstract shapes.</td>
