    <td style="padding-top:5px;color:#636362;">The fountain “chrob ou chouf”
near Ben Youssef Mosque in the
heart of the medina of Marrakech,
was brought in Ahmed
El Mansour (1578-1603). One of
the inscriptions carved invite to
“drink and watch” (in Arabic
“echroub ou chouf”). More than
just a fountain, this space invites
people to take the time to enjoy
life and watch what is happening
around them.
The idea of the workshop will be
to create a seating area (urban
furniture, chairs, sofas... ) that suggest
a pause in the daily activities.
This seat needs to be easy to
use and convenient. It should be
comfortable and ergonomic.</td>
