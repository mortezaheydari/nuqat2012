    <td colspan="5"><p>Past, present, and future are arbitrary time periods.
Under the theme of 'The Lost City of Arabesque', Nuqat design conference 2012 attempts to tackle the schism between heritage and crafts and the contemporary creative industries. The border between those realms is less clear and one is effectively derived from the other. Hence, designers and participants will further explore this notion in addition to the relation and impact their work has on defining contemporary arab identity.</p></td>
